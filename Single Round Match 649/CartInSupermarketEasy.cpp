class CartInSupermarketEasy {
    public:
    int calc(int N, int K) {
      priority_queue<int> q;
      int k = K, ans = N;
      int v = 1, n = 0;
      q.push(N);
      while(true)
      {
        int x = q.top(); q.pop();
        v--;
        if(v == 0)
        {
          n++;
          ans = min(ans, x + n);
        }
        if(x <= 2 || !k)
          break;
        k--;
        v += 2;
        q.push(x / 2);
        q.push(x - x / 2);
      }
      return ans;
    }
};

