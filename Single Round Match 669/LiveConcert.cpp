#include<bits/stdc++.h>
#define INF (1 << 30)

using namespace std;
typedef long long ll;

struct LiveConcert
{
  int maxHappiness(vector<int> h, vector<string> s)
  {
    map<string, int> m;
    int ans = 0;
    for(int i = 0; i < h.size(); i++)
    {
      string &ss = s[i];
      if(m.find(ss) == m.end())
      {
        m[ss] = h[i];
        ans += m[ss];
      }
      else
      {
        if(h[i] > m[ss])
        {
          ans -= m[ss];
          m[ss] = h[i];
          ans += m[ss];
        }
      }
    }
    return ans;
  }
};

